class AppointmentDetailsData {
  final String name;
  final String age;
  final String gender;
  final String reason;
  final String doctor;
  final String hospital;
  final String date;
  final String slot;
  final String contact;

  AppointmentDetailsData({
    required this.name,
    required this.age,
    required this.gender,
    required this.reason,
    required this.doctor,
    required this.hospital,
    required this.date,
    required this.slot,
    required this.contact,
  });
}
